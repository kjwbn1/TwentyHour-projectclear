package com.kjw.twentyhour.data;

public class Time {
    private int hour;
    private int min;
    private int houre;
    private int mine;


    public void setHour(int hour){

        this.hour = hour;

    }

    public void setMin(int min){

        this.min = min;

    }

    public void setHoure(int hour){

        this.houre = hour;

    }

    public void setMine(int min){

        this.mine = min;

    }


    public int getHour(){

        return hour;
    }

    public int getMin(){

        return min;
    }

    public int getHourE(){

        return houre;
    }

    public int getMinE(){

        return mine;
    }


}
